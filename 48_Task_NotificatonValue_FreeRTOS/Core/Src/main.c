int main(void)
{
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_USART2_UART_Init();

    xTaskCreate(Button_Task,
                "Button Task",
                200,
                NULL,
                2,
                NULL);

    xTaskCreate(LED_Task,
                "LED Task",
                200,
                NULL,
                1,
                &ledTaskHandle);

    vTaskStartScheduler();

    while(1);
}

void Button_Task(void *params)
{
    uint8_t button_state;
    uint32_t notify_value = 0;

    while(1)
    {
        button_state = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);

        if(button_state == GPIO_PIN_RESET)
        {
            vTaskDelay(pdMS_TO_TICKS(200));

            notify_value++;

            printf("Sending Notification Value: %lu\r\n", notify_value);

            xTaskNotify(ledTaskHandle,
                        notify_value,
                        eSetValueWithOverwrite);

            while(HAL_GPIO_ReadPin(GPIOC,GPIO_PIN_13)==GPIO_PIN_RESET);
        }

        vTaskDelay(pdMS_TO_TICKS(50));
    }
}

void LED_Task(void *params)
{
    uint32_t received_value;
    uint8_t state = 0;

    while(1)
    {
        xTaskNotifyWait(0,
                        0xffffffff,
                        &received_value,
                        portMAX_DELAY);

        printf("Received Value: %lu\r\n", received_value);

        switch(state)
        {
            case 0:
                HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);
                printf("LED ON\r\n");
                break;

            case 1:
                HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);
                printf("LED OFF\r\n");
                break;

            case 2:
                HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
                printf("LED TOGGLE\r\n");
                break;
        }

        state = (state + 1) % 3;   // move to next state
    }
}
